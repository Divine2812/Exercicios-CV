#Exercício 029 - Identificar a velocidade inserida, calcular a quantidade de KM por hora e o valor da multa a ser paga.
vel = int(input("Digite a velocidade do carro: "))
if vel>80:
    ac = (vel-80)*7
    print(f"A velocidade ultrapassou o limite de velocidade permitido em {vel-80}Km! O valor da multa a ser pago é de {ac:.2f} R$")
else:
    print("A velocidade inserida não ultrapassou o limite máximo permitido. =D")
